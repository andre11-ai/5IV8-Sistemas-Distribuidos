    public void Trigonometrica(){
        if (!jRadioButton1.isSelected()){
        } else {
            switch(funciones){
                case "sin":
                    valor1=Double.parseDouble(jTextField2.getText());
                    jTextField1.setText(" sin(" +valor1+" )");
                    sin = Math.sin(Math.PI*(valor1)/180);
                    jTextField2.setText(sin+" ");
                    break;
                case "asin":
                    valor1=Double.parseDouble(jTextField2.getText());
                    jTextField1.setText(" asin(" +valor1+" )");
                    asin = Math.asin(valor1)*180/Math.PI;
                    jTextField2.setText(asin+" ");
                    break;
                case "cos":
                    valor1=Double.parseDouble(jTextField2.getText());
                    jTextField1.setText(" cos(" +valor1+" )");
                    cos = Math.cos(Math.PI*(valor1)/180);
                    jTextField2.setText(cos+" ");
                    break;
                case "acos":
                    valor1=Double.parseDouble(jTextField2.getText());
                    jTextField1.setText(" acos(" +valor1+" )");
                    acos = Math.acos(valor1)*180/Math.PI;
                    jTextField2.setText(acos+" ");
                    break;
                case "tan":
                    valor1=Double.parseDouble(jTextField2.getText());
                    jTextField1.setText(" tan(" +valor1+" )");
                    tan = Math.tan(Math.PI*(valor1)/180);
                    jTextField2.setText(tan+" ");
                    break;
                case "atan":
                    valor1=Double.parseDouble(jTextField2.getText());
                    jTextField1.setText(" atan(" +valor1+" )");
                    atan = Math.atan(valor1)*180/Math.PI;
                    jTextField2.setText(atan+" ");
                    break;
            }
        }
    }
