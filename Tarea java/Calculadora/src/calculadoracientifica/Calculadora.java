    private void Operaciones(double valor1, double valor2){
        switch(tipoOperaciones){
            case "+":
                resultado = valor1 + valor2;
                jTextField2.setText(resultado + "");
                valor1 = Double.parseDouble(jTextField2.getText());
                break;
            case "-":
                resultado = valor1 - valor2;
                jTextField2.setText(resultado + "");
                valor1 = Double.parseDouble(jTextField2.getText());
                break;
            case "*":
                resultado = valor1 * valor2;
                jTextField2.setText(resultado + "");
                valor1 = Double.parseDouble(jTextField2.getText());
                break;
            case "/":
                if(valor2==0){
                    jTextField2.setText("Error");
                    break;
                }else{
                resultado = valor1 / valor2;
                jTextField2.setText(resultado + "");
                valor1 = Double.parseDouble(jTextField2.getText());
                break;
                }
        }
    }
